-- [FUNCION 1] fn_calcular_desfase_evento
CREATE OR REPLACE FUNCTION fn_calcular_desfase_evento()
RETURNS TRIGGER LANGUAGE plpgsql AS $$
DECLARE
    v_agenda         RECORD;
    v_desfase_min    INTEGER;
    v_id_res_exitoso INTEGER := 1;  -- Exitoso  (id fijo)
    v_id_res_tardio  INTEGER := 2;  -- Tardío   (id fijo)
    v_id_res_dup     INTEGER := 3;  -- Duplicado (id fijo)
    v_evento_previo  BIGINT;
    v_estado_agenda  estado_agenda_enum;
    v_regla          TEXT;
    v_detalle        TEXT;
BEGIN
    SELECT at.id_agenda, at.fecha_hora_programada, at.tolerancia_min, at.estado_agenda
    INTO v_agenda
    FROM agenda_toma at
    WHERE at.id_receta_medicamento = NEW.id_receta_medicamento
      AND at.estado_agenda IN ('pendiente', 'cumplida', 'tardia', 'omitida')
      AND NEW.timestamp_lectura BETWEEN
          at.fecha_hora_programada - (at.tolerancia_min * INTERVAL '1 minute')
          AND at.fecha_hora_programada + (at.tolerancia_min * 3 * INTERVAL '1 minute')
    ORDER BY ABS(EXTRACT(EPOCH FROM (NEW.timestamp_lectura - at.fecha_hora_programada)))
    LIMIT 1;

    IF v_agenda IS NULL THEN
        NEW.desfase_min := NULL;
        PERFORM set_config('medi_nfc2.v_id_agenda',      '0',                    TRUE);
        PERFORM set_config('medi_nfc2.v_es_duplicado',   'false',                TRUE);
        PERFORM set_config('medi_nfc2.v_regla',          'SIN_AGENDA_ASOCIABLE', TRUE);
        PERFORM set_config('medi_nfc2.v_detalle',        'No se encontró agenda pendiente en la ventana de tolerancia.', TRUE);
        PERFORM set_config('medi_nfc2.v_generar_alerta', 'false',                TRUE);
        RETURN NEW;
    END IF;

    SELECT en.id_evento INTO v_evento_previo
    FROM evento_nfc en
    WHERE en.id_receta_medicamento = NEW.id_receta_medicamento
      AND en.id_resultado IN (v_id_res_exitoso, v_id_res_tardio)
      AND en.timestamp_lectura BETWEEN
          v_agenda.fecha_hora_programada - (v_agenda.tolerancia_min * INTERVAL '1 minute')
          AND v_agenda.fecha_hora_programada + (v_agenda.tolerancia_min * 3 * INTERVAL '1 minute')
    LIMIT 1;

    IF v_evento_previo IS NOT NULL THEN
        NEW.id_resultado := v_id_res_dup;
        NEW.desfase_min  := NULL;
        PERFORM set_config('medi_nfc2.v_id_agenda',      TO_CHAR(v_agenda.id_agenda, 'FM9999999999'), TRUE);
        PERFORM set_config('medi_nfc2.v_es_duplicado',   'true',               TRUE);
        PERFORM set_config('medi_nfc2.v_regla',          'DUPLICADO_DETECTADO', TRUE);
        PERFORM set_config('medi_nfc2.v_detalle',        'Evento previo exitoso/tardío: ' || v_evento_previo || ' para agenda ' || v_agenda.id_agenda, TRUE);
        PERFORM set_config('medi_nfc2.v_generar_alerta', 'true',               TRUE);
        PERFORM set_config('medi_nfc2.v_tipo_alerta',    'Dosis Duplicada',    TRUE);
        PERFORM set_config('medi_nfc2.v_prioridad',      'Alta',               TRUE);
        RETURN NEW;
    END IF;

    v_desfase_min := ABS(EXTRACT(EPOCH FROM (NEW.timestamp_lectura - v_agenda.fecha_hora_programada)) / 60)::INTEGER;
    NEW.desfase_min := v_desfase_min;

    IF v_desfase_min <= v_agenda.tolerancia_min THEN
        NEW.id_resultado := v_id_res_exitoso;
        v_estado_agenda  := 'cumplida';
        v_regla          := 'TOMA_EXITOSA';
        v_detalle        := FORMAT('Desfase: %s min. Dentro de tolerancia (%s min).', v_desfase_min, v_agenda.tolerancia_min);
        PERFORM set_config('medi_nfc2.v_generar_alerta', 'false', TRUE);
    ELSE
        NEW.id_resultado := v_id_res_tardio;
        v_estado_agenda  := 'tardia';
        v_regla          := 'TOMA_TARDIA';
        v_detalle        := FORMAT('Desfase: %s min. Excede tolerancia (%s min).', v_desfase_min, v_agenda.tolerancia_min);
        PERFORM set_config('medi_nfc2.v_generar_alerta', 'true',       TRUE);
        PERFORM set_config('medi_nfc2.v_tipo_alerta',    'Toma Tardía', TRUE);
        PERFORM set_config('medi_nfc2.v_prioridad',      'Alta',        TRUE);
    END IF;

    UPDATE agenda_toma SET estado_agenda = v_estado_agenda WHERE id_agenda = v_agenda.id_agenda;

    UPDATE alerta a
    SET id_estado = (SELECT id_estado FROM estado_alerta WHERE UPPER(descripcion) = 'ATENDIDA')
    FROM tipo_alerta ta, estado_alerta ea
    WHERE a.id_tipo_alerta = ta.id_tipo_alerta
      AND UPPER(ta.descripcion) = 'OMISIÓN DE MEDICAMENTO'
      AND a.id_estado = ea.id_estado
      AND UPPER(ea.descripcion) = 'PENDIENTE'
      AND a.id_receta_medicamento = NEW.id_receta_medicamento;

    PERFORM set_config('medi_nfc2.v_id_agenda',    TO_CHAR(v_agenda.id_agenda, 'FM9999999999'), TRUE);
    PERFORM set_config('medi_nfc2.v_es_duplicado', 'false',    TRUE);
    PERFORM set_config('medi_nfc2.v_regla',        v_regla,    TRUE);
    PERFORM set_config('medi_nfc2.v_detalle',      v_detalle,  TRUE);
    RETURN NEW;
END;
$$;
-- [FUNCION 2] fn_registrar_alertas_y_bitacora
CREATE OR REPLACE FUNCTION fn_registrar_alertas_y_bitacora()
RETURNS TRIGGER LANGUAGE plpgsql AS $$
DECLARE
    v_generar_alerta BOOLEAN;
    v_tipo_alerta    TEXT;
    v_prioridad      TEXT;
    v_regla          TEXT;
    v_detalle        TEXT;
    v_id_tipo        INTEGER;
BEGIN
    v_generar_alerta := current_setting('medi_nfc2.v_generar_alerta', TRUE)::BOOLEAN;
    v_tipo_alerta    := current_setting('medi_nfc2.v_tipo_alerta', TRUE);
    v_prioridad      := current_setting('medi_nfc2.v_prioridad', TRUE);
    v_regla          := current_setting('medi_nfc2.v_regla', TRUE);
    v_detalle        := current_setting('medi_nfc2.v_detalle', TRUE);

    IF v_generar_alerta IS TRUE THEN
        -- Buscar id por nombre SIN depender de UPPER con tildes
        SELECT id_tipo_alerta INTO v_id_tipo
        FROM tipo_alerta
        WHERE REPLACE(REPLACE(REPLACE(LOWER(descripcion),'á','a'),'é','e'),'ó','o')
            = REPLACE(REPLACE(REPLACE(LOWER(v_tipo_alerta),'á','a'),'é','e'),'ó','o');

        IF v_id_tipo IS NOT NULL THEN
            INSERT INTO alerta(
                id_receta_medicamento, id_evento,
                id_tipo_alerta, id_estado, id_canal,
                prioridad, timestamp_gen
            ) VALUES(
                NEW.id_receta_medicamento, NEW.id_evento,
                v_id_tipo,
                (SELECT id_estado FROM estado_alerta WHERE UPPER(descripcion) = 'PENDIENTE'),
                (SELECT id_canal FROM canal_notificacion WHERE UPPER(descripcion) = 'SISTEMA'),
                v_prioridad::prioridad_alerta_enum,
                NOW()
            );
        END IF;
    END IF;

    INSERT INTO bitacora_regla_negocio(
        id_evento, regla_aplicada, resultado, detalle, timestamp_eval
    ) VALUES(
        NEW.id_evento,
        COALESCE(v_regla, 'DESCONOCIDA'),
        COALESCE((SELECT descripcion FROM resultado_validacion WHERE id_resultado = NEW.id_resultado), 'Sin clasificación'),
        COALESCE(v_detalle, ''),
        NOW()
    );

    RETURN NULL;
END;
$$;
