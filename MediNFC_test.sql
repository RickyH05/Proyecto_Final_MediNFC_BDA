                AND e.estado_etiqueta = 'activo'
          )
        ORDER BY p.nombre, m.nombre_generico;

EXCEPTION WHEN OTHERS THEN
    p_ok  := -100;
    p_msg := 'Error: ' || SQLERRM;
    OPEN io_cursor FOR SELECT p_ok AS ok, p_msg AS msg;
END;
$$;
-- [SP 60] sp_nfc_vincular

CREATE OR REPLACE PROCEDURE sp_nfc_vincular(
    IN  p_uid       VARCHAR(100),
    IN  p_rm        INTEGER,
    IN  p_cuidador  INTEGER,
    OUT p_ok        INTEGER,
    OUT p_msg       VARCHAR,
    INOUT io_cursor REFCURSOR
)
LANGUAGE plpgsql AS $$
BEGIN
    -- Validar que el UID esté pendiente
    IF NOT EXISTS (
        SELECT 1 FROM nfc_pendiente 
        WHERE uid_nfc = p_uid AND id_cuidador = p_cuidador
    ) THEN
        p_ok  := -1;
        p_msg := 'UID no encontrado en pendientes.';
        OPEN io_cursor FOR SELECT p_ok AS ok, p_msg AS msg;
        RETURN;
    END IF;

    -- Validar que el medicamento no tenga ya un NFC activo
    IF EXISTS (
        SELECT 1 FROM etiqueta_nfc 
        WHERE id_receta_medicamento = p_rm 
          AND estado_etiqueta = 'activo'
    ) THEN
        p_ok  := -2;
        p_msg := 'Este medicamento ya tiene un NFC activo.';
        OPEN io_cursor FOR SELECT p_ok AS ok, p_msg AS msg;
        RETURN;
    END IF;

    -- Insertar en etiqueta_nfc
    INSERT INTO etiqueta_nfc (uid_nfc, nombre, tipo_etiqueta, 
                               id_receta_medicamento, estado_etiqueta)
    VALUES (p_uid, 'Pastillero-' || p_uid, 'pastillero', p_rm, 'activo');

    -- Limpiar de pendientes
    DELETE FROM nfc_pendiente WHERE uid_nfc = p_uid;

    p_ok  := 1;
    p_msg := 'NFC vinculado correctamente.';
    OPEN io_cursor FOR SELECT p_ok AS ok, p_msg AS msg;

EXCEPTION 
    WHEN unique_violation THEN
        p_ok  := -10;
        p_msg := 'UID NFC duplicado.';
        OPEN io_cursor FOR SELECT p_ok AS ok, p_msg AS msg;
    WHEN OTHERS THEN
        p_ok  := -100;
        p_msg := 'Error: ' || SQLERRM;
        OPEN io_cursor FOR SELECT p_ok AS ok, p_msg AS msg;
END;
$$;
-- [FUNCION 5] fn_validar_dosis_maxima

CREATE OR REPLACE FUNCTION fn_validar_dosis_maxima()
RETURNS TRIGGER LANGUAGE plpgsql AS $$
DECLARE v_max NUMERIC;
BEGIN
    SELECT dosis_max INTO v_max FROM medicamento WHERE id_medicamento = NEW.id_medicamento;
    IF v_max IS NOT NULL AND NEW.dosis_prescrita > v_max THEN
        RAISE EXCEPTION 'Dosis % supera el máximo permitido (%) para este medicamento.',
            NEW.dosis_prescrita, v_max;
    END IF;
    RETURN NEW;
END;
$$;
