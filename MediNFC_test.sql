    OUT p_ok        INTEGER,
    OUT p_msg       VARCHAR,
    INOUT io_cursor REFCURSOR
)
LANGUAGE plpgsql AS $$
BEGIN
    p_ok  := 1;
    p_msg := 'OK';

    -- Medicamentos activos del paciente asignado al cuidador, sin etiqueta NFC
    OPEN io_cursor FOR
        SELECT
            rm.id_receta_medicamento,
