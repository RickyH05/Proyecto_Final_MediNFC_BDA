BEFORE INSERT ON receta
FOR EACH ROW EXECUTE FUNCTION fn_no_receta_duplicada();

-- [SP] sp_rep_observaciones_alertas_paciente
CREATE OR REPLACE PROCEDURE sp_rep_observaciones_alertas_paciente(
    INOUT io_cursor REFCURSOR,
    IN p_id_paciente INTEGER
)
LANGUAGE plpgsql AS $$
BEGIN
    OPEN io_cursor FOR
    SELECT
        b.id_bitacora,
        b.detalle           AS observacion,
        b.timestamp_eval    AS fecha,
        med.nombre_generico AS medicamento,
        ta.descripcion      AS tipo_alerta
    FROM bitacora_regla_negocio b
    -- JOIN directo por alerta (cubre tanto omisiones como eventos NFC)
    JOIN alerta a              ON (a.id_evento = b.id_evento OR (b.id_evento IS NULL AND a.id_alerta = (
                                    SELECT a2.id_alerta FROM alerta a2
                                    WHERE a2.id_receta_medicamento = a.id_receta_medicamento
                                    ORDER BY a2.timestamp_gen DESC LIMIT 1
                                )))
    JOIN receta_medicamento rm  ON rm.id_receta_medicamento = a.id_receta_medicamento
    JOIN medicamento med        ON med.id_medicamento = rm.id_medicamento
    JOIN tipo_alerta ta         ON ta.id_tipo_alerta = a.id_tipo_alerta
    JOIN receta r               ON r.id_receta = rm.id_receta
    WHERE r.id_paciente = p_id_paciente
      AND b.regla_aplicada = 'ALERTA_ATENDIDA'
      AND b.detalle IS NOT NULL
      AND b.detalle != ''
    ORDER BY b.timestamp_eval DESC
    LIMIT 50;
END;
$$;

