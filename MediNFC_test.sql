        100.0 * COUNT(*) FILTER (
            WHERE at.estado_agenda = 'cumplida'
        )::numeric / NULLIF(
            COUNT(*) FILTER (
                WHERE at.estado_agenda IN ('cumplida', 'tardia', 'omitida')
            ),
            0
        ),
        2
    ) AS pct
FROM receta r
    JOIN paciente p ON p.id_paciente = r.id_paciente
    JOIN receta_medicamento rm ON rm.id_receta = r.id_receta
    JOIN medicamento med ON med.id_medicamento = rm.id_medicamento
