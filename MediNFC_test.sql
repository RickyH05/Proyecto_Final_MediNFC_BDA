-- [FUNCION 3] fn_generar_agenda
CREATE OR REPLACE FUNCTION fn_generar_agenda() RETURNS TRIGGER LANGUAGE plpgsql AS $$
DECLARE v_ini DATE;
v_fin DATE;
v_dia DATE;
v_hora TIME;
v_tpd INTEGER;
v_i INTEGER;
BEGIN
SELECT r.fecha_inicio,
    r.fecha_fin INTO v_ini,
    v_fin
FROM receta r
WHERE r.id_receta = NEW.id_receta;
v_tpd := FLOOR(24.0 / NEW.frecuencia_horas);
IF v_tpd < 1 THEN v_tpd := 1;
END IF;
v_dia := v_ini;
WHILE v_dia <= v_fin LOOP v_hora := NEW.hora_primera_toma;
v_i := 0;
WHILE v_i < v_tpd LOOP
INSERT INTO agenda_toma(
        id_receta_medicamento,
        fecha_hora_programada,
        tolerancia_min,
        estado_agenda
    )
VALUES(
        NEW.id_receta_medicamento,
        v_dia + v_hora,
        NEW.tolerancia_min,
        'pendiente'
    ) ON CONFLICT(id_receta_medicamento, fecha_hora_programada) DO NOTHING;
v_hora := v_hora + (NEW.frecuencia_horas * INTERVAL '1 hour');
IF v_hora >= TIME '24:00:00' THEN EXIT;
END IF;
v_i := v_i + 1;
END LOOP;
v_dia := v_dia + 1;
END LOOP;
RETURN NEW;
END;
$$;
-- [TRIGGER 3] trg_generar_agenda
CREATE TRIGGER trg_generar_agenda
AFTER
INSERT ON receta_medicamento FOR EACH ROW EXECUTE FUNCTION fn_generar_agenda();
-- [SP 12] sp_registrar_toma_nfc
CREATE OR REPLACE PROCEDURE sp_registrar_toma_nfc(
    INOUT p_id_ev BIGINT,
    OUT p_ok INTEGER,
    OUT p_msg VARCHAR(300),
    OUT p_res VARCHAR(50),
    OUT p_prox BOOLEAN,
    INOUT io_cursor REFCURSOR,
    IN p_uid VARCHAR(100),
    IN p_cuid INTEGER,
    IN p_lat NUMERIC(10,7),
    IN p_lon NUMERIC(10,7),
    IN p_prec NUMERIC(6,2) DEFAULT NULL,
    IN p_obs VARCHAR(255) DEFAULT NULL
)
LANGUAGE plpgsql AS $$
DECLARE
    v_rm        INTEGER;
    v_pac       INTEGER;
    v_res_id    INTEGER;
    v_gps_id    BIGINT;
    v_ub_id     INTEGER;
    v_bec       RECORD;
    v_dist      NUMERIC(8,2);
    v_gps_ok    BOOLEAN := FALSE;
    v_bec_ok    BOOLEAN := FALSE;
    v_ahora     TIMESTAMP := NOW();
    v_dlat      NUMERIC;
    v_dlon      NUMERIC;
    v_a         NUMERIC;
BEGIN
    p_ok := 0; p_msg := ''; p_res := ''; p_prox := FALSE;

    SELECT id_receta_medicamento INTO v_rm
    FROM etiqueta_nfc
    WHERE uid_nfc = p_uid AND estado_etiqueta = 'activo';

    IF v_rm IS NULL THEN
        p_ok := -1; p_msg := 'Etiqueta NFC no encontrada o inactiva.';
        OPEN io_cursor FOR SELECT p_ok AS ok, p_msg AS msg;
        RETURN;
    END IF;

    SELECT r.id_paciente INTO v_pac
    FROM receta_medicamento rm
    JOIN receta r ON r.id_receta = rm.id_receta
    WHERE rm.id_receta_medicamento = v_rm;

    SELECT g.id_gps INTO v_gps_id
    FROM gps_imei g
    WHERE g.id_cuidador = p_cuid AND g.activo = TRUE
    LIMIT 1;

    IF v_gps_id IS NOT NULL AND p_lat IS NOT NULL AND p_lon IS NOT NULL THEN
        INSERT INTO ubicacion_gps(id_gps, latitud, longitud, timestamp_ubicacion)
        VALUES(v_gps_id, p_lat, p_lon, v_ahora)
        RETURNING id_ubicacion INTO v_ub_id;
        v_gps_ok := TRUE;
    END IF;

    SELECT * INTO v_bec
    FROM beacon
    WHERE id_paciente = v_pac AND activo = TRUE
    LIMIT 1;

    IF v_bec IS NOT NULL AND v_gps_ok THEN
        v_dlat := RADIANS(p_lat - v_bec.latitud_ref);
        v_dlon := RADIANS(p_lon - v_bec.longitud_ref);
        v_a := SIN(v_dlat/2)^2
             + COS(RADIANS(v_bec.latitud_ref)) * COS(RADIANS(p_lat)) * SIN(v_dlon/2)^2;
        v_dist := 6371000 * 2 * ATAN2(SQRT(v_a), SQRT(1-v_a));
        v_bec_ok := (v_dist <= v_bec.radio_metros);
    END IF;

    SELECT id_resultado INTO v_res_id
    FROM resultado_validacion WHERE id_resultado = 1;

    INSERT INTO evento_nfc(
        id_receta_medicamento, uid_nfc, timestamp_lectura,
        id_resultado, id_cuidador_lector, origen,
        observaciones, fecha_registro, hash_evento
    ) VALUES(
        v_rm, p_uid, v_ahora,
        v_res_id, p_cuid, 'nfc',
        p_obs, NOW(),
        MD5(p_uid || '-' || v_rm || '-' || v_ahora)
    )
    RETURNING id_evento INTO p_id_ev;

    INSERT INTO evento_proximidad(
        id_evento, id_ubicacion, id_beacon,
        distancia_metros, gps_verificado, beacon_detectado
    ) VALUES(
        p_id_ev, v_ub_id,
        CASE WHEN v_bec IS NOT NULL THEN v_bec.id_beacon ELSE NULL END,
        v_dist, v_gps_ok, v_bec_ok
    );

    p_prox := (v_gps_ok AND v_bec_ok);

    IF NOT p_prox AND v_gps_ok THEN
        INSERT INTO alerta(
            id_receta_medicamento, id_evento,
            id_tipo_alerta, id_estado, id_canal,
            prioridad, timestamp_gen
        ) VALUES(
            v_rm, p_id_ev,
            4,
            (SELECT id_estado FROM estado_alerta WHERE UPPER(descripcion) = 'PENDIENTE'),
            (SELECT id_canal FROM canal_notificacion WHERE UPPER(descripcion) = 'SISTEMA'),
            'Media'::prioridad_alerta_enum,
            NOW()
        );
    END IF;

    -- *** CAMBIO: distinguir SIN_AGENDA_ASOCIABLE de Exitoso real ***
    SELECT
        CASE
            WHEN b.regla_aplicada = 'SIN_AGENDA_ASOCIABLE' THEN 'Sin agenda'
            ELSE rv.descripcion
        END INTO p_res
    FROM evento_nfc en
    JOIN resultado_validacion rv ON rv.id_resultado = en.id_resultado
    JOIN bitacora_regla_negocio b ON b.id_evento = en.id_evento
    WHERE en.id_evento = p_id_ev;

    p_ok := 1;
    p_msg := 'Toma registrada ev=' || p_id_ev || ' res=' || p_res
           || ' prox=' || CASE WHEN p_prox THEN 'VÁLIDA' ELSE 'INVÁLIDA' END;

    OPEN io_cursor FOR
    SELECT en.id_evento, en.timestamp_lectura,
           rv.descripcion AS resultado, en.desfase_min,
           ep.distancia_metros, ep.proximidad_valida,
           c.nombre || ' ' || c.apellido_p AS cuidador,
           pa.nombre || ' ' || pa.apellido_p AS paciente,
           med.nombre_generico AS medicamento
    FROM evento_nfc en
    JOIN resultado_validacion rv ON rv.id_resultado = en.id_resultado
    JOIN receta_medicamento rm ON rm.id_receta_medicamento = en.id_receta_medicamento
    JOIN receta r ON r.id_receta = rm.id_receta
    JOIN paciente pa ON pa.id_paciente = r.id_paciente
    JOIN medicamento med ON med.id_medicamento = rm.id_medicamento
    LEFT JOIN cuidador c ON c.id_cuidador = en.id_cuidador_lector
    LEFT JOIN evento_proximidad ep ON ep.id_evento = en.id_evento
    WHERE en.id_evento = p_id_ev;

EXCEPTION WHEN OTHERS THEN
    p_ok := -100; p_msg := 'Error: ' || SQLERRM;
    OPEN io_cursor FOR SELECT p_ok AS ok, p_msg AS msg;
END;
$$;
-- [SP 13] sp_marcar_alerta_atendida

CREATE OR REPLACE PROCEDURE sp_marcar_alerta_atendida(
    IN p_id_al BIGINT,
    OUT p_ok INTEGER,
    OUT p_msg VARCHAR(300),
    INOUT io_cursor REFCURSOR,
    IN p_obs VARCHAR(255) DEFAULT NULL
)
LANGUAGE plpgsql AS $$
DECLARE
    v_ea_atendida   INTEGER;
    v_estado_actual VARCHAR(50);
BEGIN
    p_ok := 0; p_msg := '';

    -- ID del estado "Atendida"
    SELECT id_estado INTO v_ea_atendida
    FROM estado_alerta WHERE UPPER(descripcion) = 'ATENDIDA';

    -- Estado actual de la alerta (también valida que exista)
    SELECT ea.descripcion INTO v_estado_actual
    FROM alerta a
    JOIN estado_alerta ea ON ea.id_estado = a.id_estado
    WHERE a.id_alerta = p_id_al;

    IF NOT FOUND THEN
        p_ok := -1; p_msg := 'Alerta no encontrada.';
        OPEN io_cursor FOR SELECT p_ok AS ok, p_msg AS msg;
        RETURN;
    END IF;

    IF UPPER(v_estado_actual) = 'ATENDIDA' THEN
        p_ok := -2; p_msg := 'Esta alerta ya estaba atendida.';
        OPEN io_cursor FOR SELECT p_ok AS ok, p_msg AS msg;
        RETURN;
    END IF;

    -- Marcar como atendida
    UPDATE alerta SET id_estado = v_ea_atendida WHERE id_alerta = p_id_al;

    -- Guardar observación si la hay (id_evento puede ser NULL en omisiones)
    IF p_obs IS NOT NULL THEN
        INSERT INTO bitacora_regla_negocio(id_evento, regla_aplicada, resultado, detalle)
        SELECT a.id_evento,
               'ALERTA_ATENDIDA',
               'Atendida',
               p_obs
        FROM alerta a
        WHERE a.id_alerta = p_id_al;
    END IF;

    p_ok := 1; p_msg := 'Alerta ' || p_id_al || ' atendida.';
    OPEN io_cursor FOR
    SELECT a.id_alerta, ta.descripcion AS tipo, ea.descripcion AS estado,
           a.prioridad, a.timestamp_gen,
           pa.nombre || ' ' || pa.apellido_p AS paciente,
           med.nombre_generico AS medicamento
    FROM alerta a
    JOIN tipo_alerta ta  ON ta.id_tipo_alerta = a.id_tipo_alerta
    JOIN estado_alerta ea ON ea.id_estado = a.id_estado
    JOIN receta_medicamento rm ON rm.id_receta_medicamento = a.id_receta_medicamento
    JOIN receta r ON r.id_receta = rm.id_receta
    JOIN paciente pa ON pa.id_paciente = r.id_paciente
    JOIN medicamento med ON med.id_medicamento = rm.id_medicamento
    WHERE a.id_alerta = p_id_al;

EXCEPTION WHEN OTHERS THEN
    p_ok := -100; p_msg := 'Error: ' || SQLERRM;
    OPEN io_cursor FOR SELECT p_ok AS ok, p_msg AS msg;
END;
$$;

-- [SP 15] sp_detectar_omisiones
CREATE OR REPLACE PROCEDURE sp_detectar_omisiones(
        OUT p_ok INTEGER,
        OUT p_msg VARCHAR(300),
        OUT p_total INTEGER,
        INOUT io_cursor REFCURSOR
    ) LANGUAGE plpgsql AS $$
DECLARE r RECORD;
v_tipo INTEGER;
v_est INTEGER;
v_can INTEGER;
v_cnt INTEGER := 0;
BEGIN p_ok := 1;
p_msg := '';
p_total := 0;
SELECT id_tipo_alerta INTO v_tipo
FROM tipo_alerta
WHERE UPPER(descripcion) LIKE '%OMISI%';
SELECT id_estado INTO v_est
FROM estado_alerta
WHERE UPPER(descripcion) = 'PENDIENTE';
SELECT id_canal INTO v_can
FROM canal_notificacion
WHERE UPPER(descripcion) = 'SISTEMA';
FOR r IN
SELECT id_agenda,
    id_receta_medicamento,
    fecha_hora_programada,
    tolerancia_min
FROM agenda_toma
WHERE estado_agenda = 'pendiente'
    AND NOW() > fecha_hora_programada + (tolerancia_min * INTERVAL '1 minute') LOOP
UPDATE agenda_toma
SET estado_agenda = 'omitida'
WHERE id_agenda = r.id_agenda;
INSERT INTO alerta(
        id_receta_medicamento,
        id_evento,
        id_tipo_alerta,
        id_estado,
        id_canal,
        prioridad,
        timestamp_gen
    )
VALUES(
        r.id_receta_medicamento,
        NULL,
        v_tipo,
        v_est,
        v_can,
        'Alta'::prioridad_alerta_enum,
        NOW()
    );
v_cnt := v_cnt + 1;
END LOOP;
p_total := v_cnt;
p_msg := 'Omisiones: ' || v_cnt;
OPEN io_cursor FOR
SELECT a.id_alerta,
    a.timestamp_gen,
    pa.nombre || ' ' || pa.apellido_p AS paciente,
    med.nombre_generico AS medicamento,
    at.fecha_hora_programada AS toma_programada
FROM alerta a
    JOIN tipo_alerta ta ON ta.id_tipo_alerta = a.id_tipo_alerta
    JOIN receta_medicamento rm ON rm.id_receta_medicamento = a.id_receta_medicamento
    JOIN receta rec ON rec.id_receta = rm.id_receta
    JOIN paciente pa ON pa.id_paciente = rec.id_paciente
    JOIN medicamento med ON med.id_medicamento = rm.id_medicamento
    JOIN agenda_toma at ON at.id_receta_medicamento = rm.id_receta_medicamento
    AND at.estado_agenda = 'omitida'
WHERE UPPER(ta.descripcion) LIKE '%OMISI%'
    AND a.id_evento IS NULL
    AND a.timestamp_gen >= NOW() - INTERVAL '1 hour'
ORDER BY a.timestamp_gen DESC;
EXCEPTION
WHEN OTHERS THEN p_ok := -100;
p_msg := 'Error: ' || SQLERRM;
OPEN io_cursor FOR
SELECT p_ok AS ok,
    p_msg AS msg;
END;
$$;
