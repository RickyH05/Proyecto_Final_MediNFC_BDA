-- [SP 61] sp_login
CREATE OR REPLACE PROCEDURE sp_login(
        IN p_email VARCHAR(150),
        IN p_hash VARCHAR(255),
        OUT p_ok INTEGER,
        OUT p_msg VARCHAR(300),
        OUT p_id_usr INTEGER,
        OUT p_rol VARCHAR(30),
        OUT p_id_rol INTEGER,
        INOUT io_cursor REFCURSOR,
        IN p_ip VARCHAR(45) DEFAULT NULL
    ) LANGUAGE plpgsql AS $$
BEGIN
    p_ok     := 0;
    p_msg    := '';
    p_id_usr := NULL;
    p_rol    := NULL;
    p_id_rol := NULL;

    SELECT id_usuario, rol_usuario::TEXT,
           COALESCE(id_medico, id_cuidador)
    INTO   p_id_usr, p_rol, p_id_rol
    FROM   usuario
    WHERE  email = p_email AND password_hash = p_hash AND activo = TRUE;

    IF p_id_usr IS NULL THEN
        INSERT INTO log_acceso(id_usr, email, rol, ip, exitoso)
        SELECT id_usuario, email, rol_usuario::TEXT, p_ip, FALSE
        FROM usuario WHERE email = p_email LIMIT 1;
        p_ok  := -1;
        p_msg := 'Credenciales inválidas.';
        OPEN io_cursor FOR SELECT p_ok AS ok, p_msg AS msg;
        RETURN;
    END IF;

    UPDATE usuario SET ultimo_acceso = NOW() WHERE id_usuario = p_id_usr;
    INSERT INTO log_acceso(id_usr, email, rol, ip, exitoso)
    VALUES(p_id_usr, p_email, p_rol, p_ip, TRUE);

    p_ok  := 1;
    p_msg := 'Login OK. Rol: ' || p_rol;

    OPEN io_cursor FOR
        SELECT u.id_usuario, u.email, u.rol_usuario,
               u.id_medico, u.id_cuidador, u.activo, u.ultimo_acceso
        FROM usuario u WHERE u.id_usuario = p_id_usr;

EXCEPTION
    WHEN OTHERS THEN
        p_ok  := -100;
        p_msg := 'Error: ' || SQLERRM;
        OPEN io_cursor FOR SELECT p_ok AS ok, p_msg AS msg;
END;
$$;

-- [SP 62] sp_rep_pacientes_cuidador
CREATE OR REPLACE PROCEDURE sp_rep_pacientes_cuidador(
        INOUT io_cursor REFCURSOR,
        IN p_cuidador INTEGER,
        IN p_solo_activo BOOLEAN DEFAULT TRUE
    ) LANGUAGE plpgsql AS $$
BEGIN
    OPEN io_cursor FOR
    SELECT
        pc.id_paciente_cuidador,
        pc.id_cuidador,
        pc.id_paciente,
        p.nombre || ' ' || p.apellido_p || ' ' || COALESCE(p.apellido_m, '') AS paciente_nombre,
        p.nombre          AS nombre,
        p.apellido_p,
        p.apellido_m,
        p.fecha_nacimiento,
        p.foto_perfil,
        p.activo          AS paciente_activo,
        pc.es_principal,
        pc.activo         AS vinculo_activo
    FROM paciente_cuidador pc
    JOIN paciente p ON p.id_paciente = pc.id_paciente
    WHERE pc.id_cuidador = p_cuidador
      AND (NOT p_solo_activo OR pc.activo = TRUE)
    ORDER BY pc.es_principal DESC, p.apellido_p, p.nombre;
END;
$$;

-- [SP 63] sp_rep_grafica_tomas_cuidador
CREATE OR REPLACE PROCEDURE sp_rep_grafica_tomas_cuidador(
        INOUT io_cursor REFCURSOR,
        IN p_cuidador INTEGER,
        IN p_dias INTEGER DEFAULT 14
    ) LANGUAGE plpgsql AS $$
BEGIN
    OPEN io_cursor FOR
    SELECT
        c.id_cuidador,
        at.fecha_hora_programada::DATE              AS fecha,
        COUNT(*)                                    AS total,
        COUNT(*) FILTER (WHERE COALESCE(rv.descripcion,'Omitido') = 'Exitoso')  AS correctas,
        COUNT(*) FILTER (WHERE COALESCE(rv.descripcion,'Omitido') = 'Tardío')   AS fuera_horario,
        COUNT(*) FILTER (WHERE COALESCE(rv.descripcion,'Omitido') = 'Omitido')  AS no_tomadas,
        0                                           AS pendientes
    FROM cuidador c
    JOIN paciente_cuidador pc ON pc.id_cuidador = c.id_cuidador AND pc.activo = TRUE
    JOIN cuidador_horario ch ON ch.id_paciente_cuidador = pc.id_paciente_cuidador
    JOIN receta r ON r.id_paciente = pc.id_paciente AND r.estado_receta = 'vigente'
    JOIN receta_medicamento rm ON rm.id_receta = r.id_receta
    JOIN agenda_toma at ON at.id_receta_medicamento = rm.id_receta_medicamento
    LEFT JOIN evento_nfc en ON en.id_receta_medicamento = rm.id_receta_medicamento
        AND en.id_cuidador_lector = c.id_cuidador
        AND en.timestamp_lectura::DATE = at.fecha_hora_programada::DATE
    LEFT JOIN resultado_validacion rv ON rv.id_resultado = en.id_resultado
    WHERE c.id_cuidador = p_cuidador
        AND EXTRACT(ISODOW FROM at.fecha_hora_programada)::INT =
            CASE ch.dia_semana
                WHEN 'lunes'     THEN 1 WHEN 'martes'   THEN 2
                WHEN 'miercoles' THEN 3 WHEN 'jueves'   THEN 4
                WHEN 'viernes'   THEN 5 WHEN 'sabado'   THEN 6
                WHEN 'domingo'   THEN 7
            END
        AND at.fecha_hora_programada::TIME BETWEEN ch.hora_inicio AND ch.hora_fin
        AND at.estado_agenda != 'pendiente'
        AND COALESCE(rv.descripcion, 'x') != 'Duplicado'
        AND at.fecha_hora_programada::DATE >= CURRENT_DATE - p_dias
        AND at.fecha_hora_programada::DATE <= CURRENT_DATE
    GROUP BY c.id_cuidador, at.fecha_hora_programada::DATE
    ORDER BY fecha;
END;
$$;