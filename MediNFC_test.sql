    pa.nombre || ' ' || pa.apellido_p AS paciente,
    c.nombre || ' ' || c.apellido_p AS cuidador,
    pc.es_principal,
    pc.activo
FROM paciente_cuidador pc
    JOIN paciente pa ON pa.id_paciente = pc.id_paciente
    JOIN cuidador c ON c.id_cuidador = pc.id_cuidador
WHERE pc.id_paciente = p_pac
    AND pc.id_cuidador = p_cuid;
