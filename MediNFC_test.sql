-- [FUNCION 7] fn_no_receta_duplicada

CREATE OR REPLACE FUNCTION fn_no_receta_duplicada()
RETURNS TRIGGER LANGUAGE plpgsql AS $$
DECLARE v_count INTEGER;
BEGIN
    SELECT COUNT(*) INTO v_count
    FROM receta
    WHERE id_paciente = NEW.id_paciente
      AND id_medico   = NEW.id_medico
      AND estado_receta = 'vigente'
      AND id_receta IS DISTINCT FROM NEW.id_receta;

    IF v_count > 0 THEN
        RAISE EXCEPTION 'El paciente ya tiene una receta vigente con este médico. Cancélela primero.';
    END IF;
    RETURN NEW;
END;
$$;
