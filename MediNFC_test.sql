            ORDER BY fecha_omision
        ) AS rn
    FROM omitidas_base
),
islas AS (
    SELECT *,
        (fecha_omision - rn::INTEGER) AS grupo_isla
    FROM omitidas_numeradas
),
rachas AS (
    SELECT
        id_paciente,
        paciente,
        id_medicamento,
        medicamento,
        grupo_isla,
        MIN(fecha_omision) AS inicio_racha,
