    JOIN receta r              ON r.id_receta = rm.id_receta
    JOIN paciente p            ON p.id_paciente = r.id_paciente
    JOIN medicamento med       ON med.id_medicamento = rm.id_medicamento
    WHERE at.estado_agenda = 'omitida'
),
omitidas_numeradas AS (
    SELECT
        id_paciente,
        paciente,
        id_medicamento,
