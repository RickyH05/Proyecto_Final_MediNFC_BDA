WHERE r.id_medico = p_medico
    AND at.fecha_hora_programada >= NOW() - (p_dias * INTERVAL '1 day')
    AND at.fecha_hora_programada <= NOW()
GROUP BY p.id_paciente,
    p.nombre,
    p.apellido_p,
    p.apellido_m,
    med.nombre_generico
ORDER BY paciente,
    medicamento;
END;
