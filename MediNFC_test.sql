-- [SP 11] sp_cancelar_receta
CREATE OR REPLACE PROCEDURE sp_cancelar_receta(
        IN p_rec INTEGER,
        OUT p_ok INTEGER,
        OUT p_msg VARCHAR(300),
        INOUT io_cursor REFCURSOR
    ) LANGUAGE plpgsql AS $$ BEGIN p_ok := 0;
p_msg := '';
IF NOT EXISTS(
    SELECT 1
    FROM receta
    WHERE id_receta = p_rec
        AND estado_receta = 'vigente'
) THEN p_ok := -1;
p_msg := 'Receta no vigente.';
OPEN io_cursor FOR
SELECT p_ok AS ok,
    p_msg AS msg;
RETURN;
END IF;
UPDATE receta
SET estado_receta = 'cancelada'
WHERE id_receta = p_rec;
UPDATE agenda_toma
SET estado_agenda = 'omitida'
WHERE id_receta_medicamento IN (
        SELECT id_receta_medicamento
        FROM receta_medicamento
        WHERE id_receta = p_rec
    )
    AND estado_agenda = 'pendiente';
UPDATE etiqueta_nfc
SET estado_etiqueta = 'inactivo'
WHERE id_receta_medicamento IN (
        SELECT id_receta_medicamento
        FROM receta_medicamento
        WHERE id_receta = p_rec
    );
p_ok := 1;
p_msg := 'Receta cancelada, agendas y etiquetas desactivadas.';
OPEN io_cursor FOR
SELECT r.id_receta,
    r.estado_receta,
    COUNT(at.id_agenda) FILTER (
        WHERE at.estado_agenda = 'omitida'
    ) AS agendas_omitidas,
    COUNT(e.uid_nfc) FILTER (
        WHERE e.estado_etiqueta = 'inactivo'
    ) AS etiquetas_desactivadas
FROM receta r
    JOIN receta_medicamento rm ON rm.id_receta = r.id_receta
    LEFT JOIN agenda_toma at ON at.id_receta_medicamento = rm.id_receta_medicamento
    LEFT JOIN etiqueta_nfc e ON e.id_receta_medicamento = rm.id_receta_medicamento
WHERE r.id_receta = p_rec
GROUP BY r.id_receta,
    r.estado_receta;
