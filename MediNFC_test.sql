-- [VIEW 22] v_tendencia_adherencia_movil
CREATE OR REPLACE VIEW v_tendencia_adherencia_movil AS WITH serie_diaria AS (
        SELECT p.id_paciente,
            p.nombre || ' ' || p.apellido_p AS paciente,
            at.fecha_hora_programada::DATE AS fecha,
            COUNT(*) AS total_citas,
            COUNT(*) FILTER (
                WHERE at.estado_agenda = 'cumplida'
            ) AS cumplidas,
            COUNT(*) FILTER (
                WHERE at.estado_agenda = 'tardia'
            ) AS tardias,
            COUNT(*) FILTER (
                WHERE at.estado_agenda = 'omitida'
            ) AS omitidas,
            ROUND(
                100.0 * COUNT(*) FILTER (
                    WHERE at.estado_agenda = 'cumplida'
                ) / NULLIF(
                    COUNT(*) FILTER (
                        WHERE at.estado_agenda IN ('cumplida', 'tardia', 'omitida')
                    ),
                    0
                ),
                2
            ) AS pct_dia
        FROM paciente p
            JOIN receta r ON r.id_paciente = p.id_paciente
            JOIN receta_medicamento rm ON rm.id_receta = r.id_receta
            JOIN agenda_toma at ON at.id_receta_medicamento = rm.id_receta_medicamento
        GROUP BY p.id_paciente,
            p.nombre,
            p.apellido_p,
            at.fecha_hora_programada::DATE
    ),
    con_ventana AS (
        SELECT *,
            ROUND(
                AVG(pct_dia) OVER (
                    PARTITION BY id_paciente
                    ORDER BY fecha ROWS BETWEEN 6 PRECEDING AND CURRENT ROW
                ),
                2
            ) AS promedio_movil_7d,
            ROUND(
                AVG(pct_dia) OVER (
                    PARTITION BY id_paciente
                    ORDER BY fecha ROWS BETWEEN 13 PRECEDING AND 7 PRECEDING
                ),
                2
            ) AS promedio_movil_anterior,
            LAG(pct_dia, 1) OVER (
                PARTITION BY id_paciente
                ORDER BY fecha
            ) AS pct_dia_anterior
        FROM serie_diaria
    )
SELECT id_paciente,
    paciente,
    fecha,
    total_citas,
    cumplidas,
    tardias,
    omitidas,
    pct_dia,
    promedio_movil_7d,
    CASE
        WHEN pct_dia_anterior IS NULL THEN 'INICIO'
        WHEN promedio_movil_anterior IS NULL THEN 'INICIO'
        WHEN promedio_movil_7d < promedio_movil_anterior - 3 THEN 'DECLIVE'
        WHEN promedio_movil_7d > promedio_movil_anterior + 3 THEN 'MEJORA'
        ELSE 'ESTABLE'
    END AS tendencia
FROM con_ventana;
