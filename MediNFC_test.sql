-- [VIEW 24] v_ranking_mejora_adherencia
CREATE OR REPLACE VIEW v_ranking_mejora_adherencia AS
WITH med_reciente AS (
    SELECT
        'medico'::text AS rol,
        m.id_medico AS id_persona,
        m.nombre || ' ' || m.apellido_p || ' ' || m.apellido_m AS nombre,
        ROUND(
            100.0 * COUNT(*) FILTER (WHERE at.estado_agenda = 'cumplida')::numeric
            / NULLIF(COUNT(*) FILTER (WHERE at.estado_agenda IN ('cumplida', 'tardia', 'omitida')), 0)::numeric,
            2
        ) AS pct_reciente
    FROM medico m
    JOIN receta r              ON r.id_medico = m.id_medico
    JOIN receta_medicamento rm ON rm.id_receta = r.id_receta
    JOIN agenda_toma at        ON at.id_receta_medicamento = rm.id_receta_medicamento
    WHERE at.fecha_hora_programada >= NOW() - INTERVAL '14 days'
      AND at.fecha_hora_programada <= NOW()
    GROUP BY m.id_medico, m.nombre, m.apellido_p, m.apellido_m
),
med_anterior AS (
    SELECT
        m.id_medico AS id_persona,
        ROUND(
            100.0 * COUNT(*) FILTER (WHERE at.estado_agenda = 'cumplida')::numeric
            / NULLIF(COUNT(*) FILTER (WHERE at.estado_agenda IN ('cumplida', 'tardia', 'omitida')), 0)::numeric,
            2
        ) AS pct_anterior
    FROM medico m
    JOIN receta r              ON r.id_medico = m.id_medico
    JOIN receta_medicamento rm ON rm.id_receta = r.id_receta
    JOIN agenda_toma at        ON at.id_receta_medicamento = rm.id_receta_medicamento
    WHERE at.fecha_hora_programada >= NOW() - INTERVAL '28 days'
      AND at.fecha_hora_programada <= NOW() - INTERVAL '14 days'
    GROUP BY m.id_medico
),
cuid_base AS (
    SELECT
        c.id_cuidador,
        c.nombre || ' ' || c.apellido_p || ' ' || c.apellido_m AS nombre,
        at.fecha_hora_programada,
        COALESCE(rv.descripcion, 'Omitido') AS resultado
    FROM cuidador c
    JOIN paciente_cuidador pc  ON pc.id_cuidador = c.id_cuidador AND pc.activo = TRUE
    JOIN cuidador_horario ch   ON ch.id_paciente_cuidador = pc.id_paciente_cuidador
    JOIN receta r              ON r.id_paciente = pc.id_paciente AND r.estado_receta = 'vigente'
    JOIN receta_medicamento rm ON rm.id_receta = r.id_receta
    JOIN agenda_toma at        ON at.id_receta_medicamento = rm.id_receta_medicamento
    LEFT JOIN evento_nfc en    ON en.id_receta_medicamento = rm.id_receta_medicamento
                              AND en.id_cuidador_lector  = c.id_cuidador
                              AND en.timestamp_lectura::DATE = at.fecha_hora_programada::DATE
    LEFT JOIN resultado_validacion rv ON rv.id_resultado = en.id_resultado
    WHERE EXTRACT(ISODOW FROM at.fecha_hora_programada)::INTEGER =
          CASE ch.dia_semana
              WHEN 'lunes'     THEN 1
              WHEN 'martes'    THEN 2
              WHEN 'miercoles' THEN 3
              WHEN 'jueves'    THEN 4
              WHEN 'viernes'   THEN 5
              WHEN 'sabado'    THEN 6
              WHEN 'domingo'   THEN 7
          END
      AND at.fecha_hora_programada::TIME >= ch.hora_inicio
      AND at.fecha_hora_programada::TIME <= ch.hora_fin
      AND at.estado_agenda <> 'pendiente'
      AND COALESCE(rv.descripcion, 'x') <> 'Duplicado'
),
cuid_reciente AS (
    SELECT
        'cuidador'::text AS rol,
        id_cuidador AS id_persona,
        nombre,
        ROUND(
            100.0 * COUNT(*) FILTER (WHERE resultado IN ('Exitoso', 'Tardío'))::numeric
            / NULLIF(COUNT(*), 0)::numeric,
            2
        ) AS pct_reciente
    FROM cuid_base
    WHERE fecha_hora_programada >= NOW() - INTERVAL '14 days'
      AND fecha_hora_programada <= NOW()
    GROUP BY id_cuidador, nombre
),
cuid_anterior AS (
    SELECT
        id_cuidador AS id_persona,
        ROUND(
            100.0 * COUNT(*) FILTER (WHERE resultado IN ('Exitoso', 'Tardío'))::numeric
            / NULLIF(COUNT(*), 0)::numeric,
            2
        ) AS pct_anterior
    FROM cuid_base
    WHERE fecha_hora_programada >= NOW() - INTERVAL '28 days'
      AND fecha_hora_programada <= NOW() - INTERVAL '14 days'
    GROUP BY id_cuidador
),
union_roles AS (
    SELECT
        mr.rol,
        mr.id_persona,
        mr.nombre,
        mr.pct_reciente,
        COALESCE(ma.pct_anterior, 0::numeric) AS pct_anterior,
        (COALESCE(mr.pct_reciente, 0::numeric) - COALESCE(ma.pct_anterior, 0::numeric)) AS delta_pct
    FROM med_reciente mr
    LEFT JOIN med_anterior ma USING (id_persona)
    UNION ALL
    SELECT
        cr.rol,
        cr.id_persona,
        cr.nombre,
        cr.pct_reciente,
        COALESCE(ca.pct_anterior, 0::numeric) AS pct_anterior,
        (COALESCE(cr.pct_reciente, 0::numeric) - COALESCE(ca.pct_anterior, 0::numeric)) AS delta_pct
    FROM cuid_reciente cr
    LEFT JOIN cuid_anterior ca USING (id_persona)
)
SELECT
    rol,
    id_persona,
    nombre,
    pct_anterior,
    pct_reciente,
    ROUND(delta_pct, 2) AS delta_pct,
    RANK()       OVER (PARTITION BY rol ORDER BY delta_pct DESC) AS rank_mejora,
    DENSE_RANK() OVER (PARTITION BY rol ORDER BY delta_pct DESC) AS dense_rank_mejora,
    NTILE(4)     OVER (PARTITION BY rol ORDER BY delta_pct DESC) AS cuartil_mejora,
    CASE
        WHEN delta_pct >= 10  THEN 'MEJORA SIGNIFICATIVA'
        WHEN delta_pct >= 3   THEN 'MEJORA LEVE'
        WHEN delta_pct >= -3  THEN 'SIN CAMBIO'
        WHEN delta_pct >= -10 THEN 'DECLIVE LEVE'
        ELSE 'DECLIVE SIGNIFICATIVO'
    END AS clasificacion
FROM union_roles;
-- [SP 20] sp_gestion_horario
CREATE OR REPLACE PROCEDURE sp_gestion_horario(
        IN p_acc CHAR(1),
        INOUT p_id INTEGER,
        OUT p_ok INTEGER,
        OUT p_msg VARCHAR(300),
        INOUT io_cursor REFCURSOR,
        IN p_pac_cuid INTEGER DEFAULT NULL,
        IN p_dia dia_semana_enum DEFAULT NULL,
        IN p_inicio TIME DEFAULT NULL,
        IN p_fin TIME DEFAULT NULL
    ) LANGUAGE plpgsql AS $$ BEGIN p_ok := 0;
p_msg := '';
IF UPPER(p_acc) = 'I' THEN IF p_pac_cuid IS NULL
OR p_dia IS NULL
OR p_inicio IS NULL
OR p_fin IS NULL THEN p_ok := -1;
p_msg := 'Campos obligatorios incompletos.';
OPEN io_cursor FOR
SELECT p_ok AS ok,
    p_msg AS msg;
RETURN;
END IF;
IF p_fin <= p_inicio THEN p_ok := -2;
p_msg := 'hora_fin debe ser mayor que hora_inicio.';
OPEN io_cursor FOR
SELECT p_ok AS ok,
    p_msg AS msg;
RETURN;
END IF;
IF NOT EXISTS (
    SELECT 1
    FROM paciente_cuidador
    WHERE id_paciente_cuidador = p_pac_cuid
        AND activo = TRUE
) THEN p_ok := -3;
p_msg := 'Vínculo paciente-cuidador no encontrado o inactivo.';
OPEN io_cursor FOR
SELECT p_ok AS ok,
    p_msg AS msg;
RETURN;
END IF;
INSERT INTO cuidador_horario (
        id_paciente_cuidador,
        dia_semana,
        hora_inicio,
        hora_fin
    )
VALUES (p_pac_cuid, p_dia, p_inicio, p_fin)
RETURNING id_cuidador_horario INTO p_id;
p_ok := 1;
p_msg := 'Turno agregado id=' || p_id;
OPEN io_cursor FOR
SELECT ch.id_cuidador_horario,
    ch.dia_semana,
    ch.hora_inicio,
    ch.hora_fin,
    c.nombre || ' ' || c.apellido_p AS cuidador,
    p.nombre || ' ' || p.apellido_p AS paciente
FROM cuidador_horario ch
    JOIN paciente_cuidador pc ON pc.id_paciente_cuidador = ch.id_paciente_cuidador
    JOIN cuidador c ON c.id_cuidador = pc.id_cuidador
    JOIN paciente p ON p.id_paciente = pc.id_paciente
WHERE ch.id_cuidador_horario = p_id;
ELSIF UPPER(p_acc) = 'D' THEN IF p_id IS NULL THEN p_ok := -3;
p_msg := 'Falta p_id.';
OPEN io_cursor FOR
SELECT p_ok AS ok,
    p_msg AS msg;
RETURN;
END IF;
IF NOT EXISTS (
    SELECT 1
    FROM cuidador_horario
    WHERE id_cuidador_horario = p_id
) THEN p_ok := -4;
p_msg := 'Turno no encontrado.';
OPEN io_cursor FOR
SELECT p_ok AS ok,
    p_msg AS msg;
RETURN;
END IF;
DELETE FROM cuidador_horario
WHERE id_cuidador_horario = p_id;
p_ok := 1;
p_msg := 'Turno eliminado.';
OPEN io_cursor FOR
SELECT p_ok AS ok,
    p_msg AS msg;
ELSIF UPPER(p_acc) = 'L' THEN IF p_pac_cuid IS NULL THEN p_ok := -1;
p_msg := 'Falta p_pac_cuid para listar horarios.';
OPEN io_cursor FOR
SELECT p_ok AS ok,
    p_msg AS msg;
RETURN;
END IF;
p_id := NULL;
p_ok := 1;
p_msg := 'Horarios listados.';
OPEN io_cursor FOR
SELECT ch.id_cuidador_horario,
    ch.dia_semana,
    ch.hora_inicio,
    ch.hora_fin,
    c.nombre || ' ' || c.apellido_p AS cuidador,
    p.nombre || ' ' || p.apellido_p AS paciente
FROM cuidador_horario ch
    JOIN paciente_cuidador pc ON pc.id_paciente_cuidador = ch.id_paciente_cuidador
    JOIN cuidador c ON c.id_cuidador = pc.id_cuidador
    JOIN paciente p ON p.id_paciente = pc.id_paciente
WHERE ch.id_paciente_cuidador = p_pac_cuid
ORDER BY CASE
        ch.dia_semana
        WHEN 'lunes' THEN 1
        WHEN 'martes' THEN 2
        WHEN 'miercoles' THEN 3
        WHEN 'jueves' THEN 4
        WHEN 'viernes' THEN 5
        WHEN 'sabado' THEN 6
        WHEN 'domingo' THEN 7
    END,
    ch.hora_inicio;
ELSE p_ok := -99;
p_msg := 'Acción no válida. Use I, D o L.';
OPEN io_cursor FOR
SELECT p_ok AS ok,
    p_msg AS msg;
END IF;
EXCEPTION
WHEN OTHERS THEN p_ok := -100;
p_msg := 'Error: ' || SQLERRM;
OPEN io_cursor FOR
SELECT p_ok AS ok,
    p_msg AS msg;
END;
$$;
