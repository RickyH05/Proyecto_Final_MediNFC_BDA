-- [SP 30] sp_rep_adherencia_pacientes_medico
CREATE OR REPLACE PROCEDURE sp_rep_adherencia_pacientes_medico(
        INOUT io_cursor REFCURSOR,
        IN p_medico INTEGER,
        IN p_dias INTEGER DEFAULT 14
    ) LANGUAGE plpgsql AS $$ BEGIN OPEN io_cursor FOR
SELECT p.id_paciente,
    (
        p.nombre || ' ' || p.apellido_p || ' ' || COALESCE(p.apellido_m, '')
    ) AS paciente,
    med.nombre_generico AS medicamento,
    COUNT(at.id_agenda) AS total,
