    id_cuidador,
    cuidador,
    fecha_hora_programada,
    resultado AS estado_agenda,
    COUNT(*) OVER (PARTITION BY id_cuidador) AS total,
    COUNT(*) FILTER (WHERE resultado = 'Exitoso') OVER (PARTITION BY id_cuidador) AS ok,
    COUNT(*) FILTER (WHERE resultado = 'Tardío')  OVER (PARTITION BY id_cuidador) AS tarde,
    COUNT(*) FILTER (WHERE resultado = 'Omitido') OVER (PARTITION BY id_cuidador) AS omitida,
    ROUND(
        100.0 * COUNT(*) FILTER (WHERE resultado IN ('Exitoso', 'Tardío')) OVER (PARTITION BY id_cuidador)::numeric
